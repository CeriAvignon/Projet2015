package fr.univavignon.courbes.src.mp;


public class Position {
	private int x;
	private int y;
	
	public Position(int X,int Y){
		x= X;
		y= Y;
	}
	
	public void setX(int x) {this.x = x;}
	public int getX() {return x;}
	public void setY(int y) {this.y = y;}
	public int getY()	 {return y;}
}
